# Voeg de rdl file toe
1. Selecteer de rdl file in Solution Exoplorer van VS2010
2. Kies de rechter muisoptie "Git (<feature naam>) add files"
3. geef het git commando commit in Visual Studio

# Dit zorgt er voor dat alle files in subdirectorie van de solution en de files
# die in dezelfde directory staan als de sln file worden toegevoegd en onder versiebeheer komen van git
