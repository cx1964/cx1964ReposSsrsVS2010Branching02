# SSRS wijzigingen in VS2010 kunnen alleen met een commit vanuit VS2010 gegeven worden en niet via de commandline.
1. Selecteer het project in Solution Explorer van VS2010
2. Commit de wijzigingen van SSRS via de rechter menuoptie in Solution Explorer mbv de optie  "Git (<feature naam>)->commit"
3. Vul de tekst in waarom welke wijzigen worden gecommit.
4. Druk op de knop commit
5. Sluit Visual Studio af

# Onderstaand commando alleen voor text files
# git commit -m "commit alle wijzigingen aan ssrs rapport TestSSRSReportVS2010_kostenplaatsen"
